# -*- coding: utf-8 -*-

hello_text = "hello world"

# <class 'str'>
print(type(hello_text))

# <class 'int'>
print(type(12))
print(type(12))
print(type(0x12))

# <class 'float'>
print(type(12.11))

# <class 'bool'>
print(type(True))
print(type(False))

# print(true)

# <class 'complex'>
print(type(1 + 2j))

# + 运算符
print(1 + 1.2)

print("hello" + " " + "world")

# print("hi, " + 11)
print("hi, " + str(11))


